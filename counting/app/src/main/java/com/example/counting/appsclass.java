package com.example.counting;

public class appsclass {
    int images;
    String count;

    public appsclass(int images, String count) {
        this.images = images;
        this.count = count;
    }

    public int getImages() {
        return images;
    }

    public void setImages(int images) {
        this.images = images;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
